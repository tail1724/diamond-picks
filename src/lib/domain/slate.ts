const ml = (side: Side, american: number): MarketQuote => ({ kind: "moneyline", side, american });
const tot = (side: Side, line: number, american: number): MarketQuote => ({
  kind: "total",
  side,
  line,
  american,
});
const pk = (playerId: string, side: Side, line: number, american: number): MarketQuote => ({
  kind: "pitcher_k",
  side,
  line,
  playerId,
  american,
});
const per = (playerId: string, side: Side, line: number, american: number): MarketQuote => ({
  kind: "pitcher_er",
  side,
  line,
  playerId,
  american,
});
const htb = (playerId: string, line: number, american: number): MarketQuote => ({
  kind: "hitter_tb",
  side: "over",
  line,
  playerId,
  american,
});

