export function pitcher(id: string): Pitcher {
  const p = PITCHERS[id];
  if (!p) throw new Error(`Unknown pitcher: ${id}`);
  return p;
}
export function hitter(id: string): Hitter {
  const h = HITTERS[id];
  if (!h) throw new Error(`Unknown hitter: ${id}`);
  return h;
}
export function gameById(id: string): Game | undefined {
  return SLATE.games.find((g) => g.id === id);
}
